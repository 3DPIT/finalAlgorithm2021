int solution(vector<string> holidays, int k) {
	int answer = -1;
	for (int i = 0; i < holidays.size(); i++) {
		string a = "";//날짜 산출을 위한 것
		a += holidays[i][0];
		a += holidays[i][1];
		a += holidays[i][3];
		a += holidays[i][4];

		//날짜 휴일 체크 
		for (int i = 0; i < 365; i++) {
			if (cal[i].day == a) {
				cal[i].num = 1;
			}
		}
	}
	int vacation[365] = { 0, };
	//휴일 길이 체크
	for (int i = 0; i < 365; i++) {
		int cnt = 0;
		int idx = i;
		while(cal[idx].num==1&&idx<=364) {//개수 체크
			cnt++;
			idx++;
		}
		i = idx--;
		if (cnt != 0) {
			vacation[cnt]++;//체크 
		}
	}
	int rank[365] = { 0, };// 순위 등록
	int idx = 0;
	for (int i = 364; i >= 0; i--) {
		if (vacation[i] != 0) {
			rank[++idx] = i;
		}
	}
	answer = rank[k];
	return answer;
}
